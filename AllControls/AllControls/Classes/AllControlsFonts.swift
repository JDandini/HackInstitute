import UIKit

extension UIFont{
    class func exo2(size:CGFloat)->UIFont{
        return UIFont(name: "Exo2-Regular", size: size)!
    }
    class func exo2Bold(size:CGFloat)->UIFont{
        return UIFont(name: "Exo2-Bold", size: size)!
    }
    class func exo2ExtraLight(size:CGFloat)->UIFont{
        return UIFont(name: "Exo2-ExtraLight", size: size)!
    }
    class func exo2Thin(size:CGFloat)->UIFont{
        return UIFont(name: "Exo2-Thin", size: size)!
    }
}
